# Програма для знаходження коренів квадратного рівняння ax^2 + bx + c = 0

# Введення коефіцієнтів
a = float(input("Введіть коефіцієнт a: "))
b = float(input("Введіть коефіцієнт b: "))
c = float(input("Введіть коефіцієнт c: "))

# Обчислення дискримінанта
discriminant = b**2 - 4*a*c

# Визначення коренів рівняння
if discriminant > 0:
    # Два дійсних корені
    x1 = (-b + discriminant**0.5) / (2*a)
    x2 = (-b - discriminant**0.5) / (2*a)
    print("Рівняння має два дійсні корені:")
    print(f"x1 = {x1}")
    print(f"x2 = {x2}")
elif discriminant == 0:
    # Один дійсний корінь
    x = -b / (2*a)
    print("Рівняння має один дійсний корінь:")
    print(f"x = {x}")
else:
    # Немає дійсних коренів
    print("Рівняння не має дійсних коренів.")